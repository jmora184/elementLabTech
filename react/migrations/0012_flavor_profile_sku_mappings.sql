INSERT OR IGNORE INTO flavor_profile_sku_mappings (
  id,
  primary_profile_id,
  secondary_profile_id,
  sort_order,
  is_active,
  created_at,
  updated_at
)
SELECT
  lower(hex(randomblob(16))) AS id,
  primary_fp.id AS primary_profile_id,
  secondary_fp.id AS secondary_profile_id,
  normalized.sort_order,
  1 AS is_active,
  datetime('now') AS created_at,
  datetime('now') AS updated_at
FROM normalized
JOIN flavor_profiles AS primary_fp
  ON primary_fp.name = normalized.primary_name
JOIN flavor_profiles AS secondary_fp
  ON secondary_fp.name = normalized.secondary_name
WHERE primary_fp.id <> secondary_fp.id;
