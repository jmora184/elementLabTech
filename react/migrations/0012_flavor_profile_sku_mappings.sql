-- Primary -> secondary flavor/SKU mappings for the Build Your Flavor Library widget.
-- This keeps the product page from showing the entire collection catalog and instead
-- shows only the mapped companion options for the selected primary flavor.

CREATE TABLE IF NOT EXISTS flavor_profile_sku_mappings (
  id TEXT PRIMARY KEY,
  primary_profile_id TEXT NOT NULL,
  secondary_profile_id TEXT NOT NULL,
  sort_order INTEGER NOT NULL DEFAULT 0,
  is_active INTEGER NOT NULL DEFAULT 1 CHECK (is_active IN (0, 1)),
  created_at TEXT NOT NULL DEFAULT (datetime('now')),
  updated_at TEXT NOT NULL DEFAULT (datetime('now')),
  FOREIGN KEY (primary_profile_id) REFERENCES flavor_profiles(id) ON DELETE CASCADE,
  FOREIGN KEY (secondary_profile_id) REFERENCES flavor_profiles(id) ON DELETE CASCADE,
  UNIQUE (primary_profile_id, secondary_profile_id)
);

CREATE INDEX IF NOT EXISTS idx_flavor_profile_sku_mappings_primary
  ON flavor_profile_sku_mappings(primary_profile_id, is_active, sort_order, created_at);

CREATE INDEX IF NOT EXISTS idx_flavor_profile_sku_mappings_secondary
  ON flavor_profile_sku_mappings(secondary_profile_id, is_active, sort_order, created_at);

